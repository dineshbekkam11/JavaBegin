package Class4;

public class Array_1 {
    public static void main(String[] args) {
        String[] S = {"Dinu", "Somu", "Anna", "hhh"};
        int size = S.length;
        for (int i = 0; i <= size; i++)
        {
            if (i % 2 == 0) {
                System.out.println(S[i]);
            }
            else
            {
                System.out.println(S[i]);
            }
        }
    }
}

